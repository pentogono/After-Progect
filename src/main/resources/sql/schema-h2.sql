DROP TABLE IF EXISTS brevets;

CREATE TABLE IF NOT EXISTS brevets (
id INTEGER PRIMARY KEY AUTO_INCREMENT,
name VARCHAR(255) NOT NULL,
link_map VARCHAR(255) NOT NULL
);

DROP TABLE IF EXISTS randonneurs_2017;

CREATE TABLE IF NOT EXISTS randonneurs_2017 (
name VARCHAR(255) NOT NULL,
distance INTEGER PRIMARY KEY AUTO_INCREMENT,
time VARCHAR(255) NOT NULL,
);

